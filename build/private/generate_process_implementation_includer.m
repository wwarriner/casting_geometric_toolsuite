function generate_process_implementation_includer( out_folder )

names = get_process_implementation_names();
joined_names = join( names + "();" + newline, "" );
file_contents = [ ...
    'function process_implementation_includer()' newline ...
    newline ...
    joined_names{:} ...
    newline ...
    'end' ...
    ];
output_file = fullfile( out_folder, 'process_implementation_includer.m' );
write_text_file( output_file, file_contents );

end

